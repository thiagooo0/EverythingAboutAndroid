    /**
     * 用hashMap去寻找最多的数
     */
    private fun majorityElement1(nums: IntArray): Int {
        var maxNum = nums[0]
        var time = 1
        val map = HashMap<Int, Int>()
        map[maxNum] = 1
        for (n in 1 until nums.size) {
            val num = nums[n]
            if (map.containsKey(num)) {
                val t = map[num]!!
                if (t + 1 > time) {
                    maxNum = num
                    time = t + 1
                }
                if (time > (nums.size / 2 + 1)) {
                    break
                }
                map[num] = t + 1

            } else {
                map[num] = 1
            }
        }
        return maxNum
    }

    /**
     * 摩尔投票：抵消法。
     * 由于目标数超过数组长度的一半，所以就算数组中不同的元素都两两抵消，到最后剩下来的肯定是目标数
     */
    private fun majorityElement2(nums: IntArray): Int {
        var currentNum = nums[0]
        var vote = 1
        for (n in 1 until nums.size) {
            val num = nums[n]
            if (num == currentNum) {
                vote++
            } else {
                vote--
                if (vote < 0) {
                    currentNum = num
                    vote = 0
                }
            }
        }
        return currentNum
    }
